// Helper function to get translation
export const getTranslation = (language, key, subKey = null) => {
  const langData = translations[language];
  if (!langData) return key;
  
  if (subKey) {
    return langData[key]?.[subKey] || key;
  }
  
  return langData[key] || key;
}; 